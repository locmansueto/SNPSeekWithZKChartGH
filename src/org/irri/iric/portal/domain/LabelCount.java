package org.irri.iric.portal.domain;


public class LabelCount {

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public Long getCount() {
		return count;
	}

	public void setCount(Long count) {
		this.count = count;
	}

	private String label;
	private Long count;

}
